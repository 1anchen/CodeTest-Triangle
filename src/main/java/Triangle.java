public class Triangle {

    int sideA;
    int sideB;
    int sideC;

    public Triangle(int numberOne, int numberTwo, int numberThree){

        this.sideA=numberOne;
        this.sideB=numberTwo;
        this.sideC=numberThree;

    }

    public int getSideA() {
        return this.sideA;
    }

    public int getSideB() {
        return this.sideB;
    }

    public int getSideC() {
        return this.sideC;
    }
}
